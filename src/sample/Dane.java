package sample;

/**
 * The type Dane.
 */
public class Dane {
    private Integer x;
    private Integer y;

    /**
     * Instantiates a new Dane.
     */
    public Dane() {
    }

    /**
     * Instantiates a new Dane.
     *
     * @param x the x
     * @param y the y
     */
    public Dane(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Gets x.
     *
     * @return the x
     */
    public Integer getX() {
        return x;
    }

    /**
     * Sets x.
     *
     * @param x the x
     */
    public void setX(Integer x) {
        this.x = x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public Integer getY() {
        return y;
    }

    /**
     * Sets y.
     *
     * @param y the y
     */
    public void setY(Integer y) {
        this.y = y;
    }
}
